def _sanitize_code_for_notebook(code: str) -> str:
    """Remove characters that would corrupt a notebook cell or its JSON encoding."""
    if not code:
        return ""
    # Strip to ASCII only â€” removes emoji and other multi-byte symbols
    code = code.encode("ascii", errors="ignore").decode("ascii")
    # Remove null bytes
    code = code.replace("\x00", "")
    # Remove control characters except newline (\n) and tab (\t)
    code = re.sub(r"[\x01-\x08\x0b\x0c\x0e-\x1f\x7f]", "", code)
    return code


def _fix_json_newlines(s: str) -> str:
    """
    Escape literal newlines/carriage returns that appear INSIDE JSON string values.

    Claude sometimes returns JSON like:
        {"code": "line1
    line2"}
    instead of:
        {"code": "line1\\nline2"}

    This breaks json.loads. We fix it by walking the string and escaping
    any bare newlines that occur while we're inside a JSON string.
    """
    result = []
    in_string = False
    escape_next = False
    for ch in s:
        if escape_next:
            result.append(ch)
            escape_next = False
        elif ch == "\\" and in_string:
            result.append(ch)
            escape_next = True
        elif ch == '"':
            in_string = not in_string
            result.append(ch)
        elif ch == "\n" and in_string:
            result.append("\\n")
        elif ch == "\r" and in_string:
            result.append("\\r")
        else:
            result.append(ch)
    return "".join(result)


